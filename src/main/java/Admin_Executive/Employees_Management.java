    private void import_data_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_import_data_btnActionPerformed
        // TODO add your handling code here:
        File file = new File ("C:\\Users\\Kingston\\Documents\\NetBeansProjects\\Assignment\\employees_management.txt");
        try {
            BufferedReader br = new BufferedReader(new FileReader(file));
            String line = br.readLine();
            String [] colHeadings = line.trim().split(":");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setColumnIdentifiers(colHeadings);

            Object [] dataRows = br.lines().toArray(); //it will read all the lines in the file in array
            System.out.println("No of records : "+ dataRows.length);
            for (int i = 0; i < dataRows.length; i++){
                String rec = (dataRows[i].toString());
                String [] dataRow = rec.split(":");
                model.addRow(dataRow);
            } br.close();
        } catch (Exception ex) {
            Logger.getLogger(Employees_Management.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_import_data_btnActionPerformed

