    private void save_new_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_new_btnActionPerformed
        // TODO add your handling code here:
        String id = resident_tenant_id.getText().toString();
        String name = resident_tenant_name.getText().toString();
        String number = unit_number.getText().toString();
        String type = property_type.getSelectedItem().toString();

        if(resident_tenant_id.getText().isEmpty() || resident_tenant_name.getText().isEmpty() || unit_number.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Please fill all the fields");
        }else{

            try {
                FileWriter writer = new FileWriter("resident_tenant_management.txt", true);
                writer.write(""+id+":"+name+":"+number+":"+type+"\n");
                writer.close();
                JOptionPane.showMessageDialog(rootPane, "Success");
                System.exit(0);

            } catch (Exception e){

                JOptionPane.showMessageDialog(rootPane, "Error");
            }
        }
    }//GEN-LAST:event_save_new_btnActionPerformed

