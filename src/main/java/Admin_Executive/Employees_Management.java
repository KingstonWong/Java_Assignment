    private void save_data_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_data_btnActionPerformed
        // TODO add your handling code here:
        File file = new File ("C:\\Users\\Kingston\\Documents\\NetBeansProjects\\Assignment\\employees_management.txt");
        try {
            String line = "";
            String lines = "";
            int index;
            BufferedWriter bw = new BufferedWriter(new FileWriter(file));
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            String colHeadings = "";
            for (int i = 0; i < model.getColumnCount(); i++){
                colHeadings = colHeadings + model.getColumnName(i) + ":" ;
            }

            index = colHeadings.lastIndexOf(":");
            StringBuilder Line = new StringBuilder(colHeadings);
            Line.setCharAt(index, ' ');

            bw.write(Line + "\n");

            for (int row = 0; row < model.getRowCount(); row++){
                for (int col = 0; col < model.getColumnCount(); col++)
                line =  line + model.getValueAt(row, col) + ":" ;
                index = line.lastIndexOf(":");
                Line = new StringBuilder(line);
                Line.setCharAt(index, ' ');
                lines += Line.toString();
                line = "";

                bw.write(Line.toString().trim() + "\n");
            } bw.close();
        } catch (IOException ex) {
            Logger.getLogger(Employees_Management.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_save_data_btnActionPerformed

